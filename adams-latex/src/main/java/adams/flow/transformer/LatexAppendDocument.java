public class LatexAppendDocument
  extends AbstractTransformer {

  private static final long serialVersionUID = 8176319006728477554L;

  /** the code generator to use. */
  protected AbstractCodeGenerator m_Generator;

  /**
   * Returns a string describing the object.
   *
   * @return 			a description suitable for displaying in the gui
   */
  @Override
  public String globalInfo() {
    return "Appends the text generated from the specified LaTeX code generator.";
  }

  /**
   * Adds options to the internal list of options.
   */
  @Override
  public void defineOptions() {
    super.defineOptions();

    m_OptionManager.add(
      "generator", "generator",
      new CustomStatements());
  }

  /**
   * Sets the generator to use.
   *
   * @param value	the generator
   */
  public void setGenerator(AbstractCodeGenerator value) {
    m_Generator = value;
    reset();
  }

  /**
   * Returns the generator to use.
   *
   * @return		the generator
   */
  public AbstractCodeGenerator getGenerator() {
    return m_Generator;
  }

  /**
   * Returns the tip text for this property.
   *
   * @return 		tip text for this property suitable for
   * 			displaying in the GUI or for listing the options.
   */
  public String generatorTipText() {
    return "The code generator to use for appending the document.";
  }

  /**
   * Returns a quick info about the actor, which will be displayed in the GUI.
   *
   * @return		null if no info available, otherwise short string
   */
  @Override
  public String getQuickInfo() {
    return QuickInfoHelper.toString(this, "generator", m_Generator, "generator: ");
  }

  /**
   * Returns the class that the consumer accepts.
   *
   * @return		the Class of objects that can be processed
   */
  @Override
  public Class[] accepts() {
    return new Class[]{String.class, StringBuilder.class};
  }

  /**
   * Returns the class of objects that it generates.
   *
   * @return		the Class of the generated tokens
   */
  @Override
  public Class[] generates() {
    return new Class[]{StringBuilder.class};
  }

  /**
   * Executes the flow item.
   *
   * @return		null if everything is fine, otherwise error message
   */
  @Override
  protected String doExecute() {
    String		result;
    StringBuilder	doc;

    result = null;
    try {
      if (m_InputToken.getPayload() instanceof StringBuilder)
	doc = (StringBuilder) m_InputToken.getPayload();
      else
	doc = new StringBuilder((String) m_InputToken.getPayload());
      m_Generator.setFlowContext(this);
      doc.append(m_Generator.generate());
      m_OutputToken = new Token(doc);
    }
    catch (Exception e) {
      result = handleException("Failed to append document!", e);
    }

    return result;
  }
}
